USE bte;

CREATE TABLE IF NOT EXISTS `bte_inventory` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`partnum` VARCHAR(40) NULL DEFAULT NULL,
	`title` VARCHAR(120) NULL DEFAULT NULL,
	`selling_cost` VARCHAR(10) NULL DEFAULT NULL,
	`type` VARCHAR(20) NULL DEFAULT NULL,
	`fba_allocation` VARCHAR(10) NULL DEFAULT NULL,
	`notes` VARCHAR(100) NULL DEFAULT NULL,
	`condition` VARCHAR(10) NULL DEFAULT NULL,
	`qty` VARCHAR(10) NULL DEFAULT NULL,
	`weight` VARCHAR(10) NULL DEFAULT NULL,
	`upc` VARCHAR(20) NULL DEFAULT NULL,
	`mfr` VARCHAR(40) NULL DEFAULT NULL,
	`mpn` VARCHAR(40) NULL DEFAULT NULL,
	`length` VARCHAR(10) NULL DEFAULT NULL,
	`width` VARCHAR(10) NULL DEFAULT NULL,
	`depth` VARCHAR(10) NULL DEFAULT NULL,
	`purchase_price` VARCHAR(20) NULL DEFAULT NULL,
	`total` VARCHAR(20) NULL DEFAULT NULL,
	PRIMARY KEY (`id`),
	UNIQUE INDEX `partnum` (`partnum`)
) COLLATE='utf8_general_ci' ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `bte_inventory_change` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`order_date` DATE NOT NULL,
	`channel` VARCHAR(20) NOT NULL,
	`order_id` VARCHAR(40) NOT NULL,
	`change` VARCHAR(20) NOT NULL,
	`partnum` VARCHAR(40) NOT NULL,
	`title` VARCHAR(120) NOT NULL,
	`selling_cost` VARCHAR(10) NOT NULL,
	`type` VARCHAR(20) NOT NULL,
	`fba_allocation` VARCHAR(10) NOT NULL,
	`notes` VARCHAR(100) NOT NULL,
	`condition` VARCHAR(10) NOT NULL,
	`qty` VARCHAR(10) NOT NULL,
	`weight` VARCHAR(10) NOT NULL,
	`upc` VARCHAR(20) NOT NULL,
	`mfr` VARCHAR(40) NOT NULL,
	`mpn` VARCHAR(40) NOT NULL,
	`length` VARCHAR(10) NOT NULL,
	`width` VARCHAR(10) NOT NULL,
	`depth` VARCHAR(10) NOT NULL,
	`purchase_price` VARCHAR(20) NOT NULL,
	`total` VARCHAR(20) NOT NULL,
	PRIMARY KEY (`id`)
) COLLATE='utf8_general_ci' ENGINE=InnoDB;
